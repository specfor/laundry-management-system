<script>
    let navDropdownMenu = document.getElementById('navbar-dropdown')
    let navDropdownBtn = document.getElementById('nav-dropdown')
    let navDropdownShow = document.getElementById('nav-dropdown-show')
    let navDropdownHide = document.getElementById('nav-dropdown-hide')
    navDropdownBtn.addEventListener('click', hideDropdownMenu)

    function hideDropdownMenu() {
        navDropdownShow.classList.toggle('hidden')
        navDropdownHide.classList.toggle('hidden')
        navDropdownMenu.classList.toggle('hidden')
    }

    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
                e.preventDefault()
                if (!navDropdownMenu.classList.contains('hidden'))
                    hideDropdownMenu()
                document.querySelector(this.getAttribute('href')).scrollIntoView({behavior: 'smooth'})
            }
        )
    })

    function showZoomedImage(event) {
        document.getElementById('image-viewer-img').src = event.target.src
        document.getElementById('image-viewer').classList.remove('hidden')
        console.log('added')
    }

    function closeImage(){
        document.getElementById('image-viewer').classList.add('hidden')
    }
</script>
